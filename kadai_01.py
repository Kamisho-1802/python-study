# 課題 5: 辞書を使おう
# ============================================
#
# 【目標】
#   以下の商品リスト（辞書のリスト）を使って、
#   3つの処理を行ってください。
#
# 【データ】
#   products = [
#       {"name": "りんご",   "price": 150, "stock": 30},
#       {"name": "バナナ",   "price": 80,  "stock": 0},
#       {"name": "みかん",   "price": 120, "stock": 15},
#       {"name": "ぶどう",   "price": 400, "stock": 8},
#       {"name": "メロン",   "price": 800, "stock": 0},
#       {"name": "いちご",   "price": 350, "stock": 20},
#   ]
#
# 【やること】
#   1. 全商品の名前と価格を表示する
#      例) りんご: 150円
#
#   2. 在庫あり（stock > 0）の商品名だけ表示する
#
#   3. 全商品の在庫合計金額を計算して表示する
#      （price × stock の合計）
#      例) 在庫合計金額: ○○円
#
# 【辞書のメモ】
#   PHPの連想配列と同じ感覚です。
#   product["name"]   ← 値を取得
#   for p in products: ← リストをループ
#
# ============================================

products = [
    {"name": "りんご",   "price": 150, "stock": 30},
    {"name": "バナナ",   "price": 80,  "stock": 0},
    {"name": "みかん",   "price": 120, "stock": 15},
    {"name": "ぶどう",   "price": 400, "stock": 8},
    {"name": "メロン",   "price": 800, "stock": 0},
    {"name": "いちご",   "price": 350, "stock": 20},
]

# 1. 全商品の名前と価格を表示
print("全商品の名前と価格:")
for p in products:
    print(f"{p['name']}: {p['price']}円")

# 2. 在庫ありの商品名だけ表示
print("\n在庫ありの商品:")
for p in products:
    if p["stock"] > 0:
        print(p["name"])

# 3. 在庫合計金額を計算して表示
print("\n在庫合計金額:")
total= 0
for p in products:
    total = p["price"] * p["stock"]

print(f"{p['name']}: {total}円")

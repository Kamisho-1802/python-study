# 課題 4: リストを操作しよう
# ============================================
#
# 【目標】
#   以下のリストを使って、3つの処理を行ってください。
#
#   scores = [45, 82, 91, 60, 73, 55, 88, 76, 40, 95]
#
#   1. 全スコアの「合計」を表示する
#   2. 全スコアの「平均」を表示する
#   3. 「80点以上」のスコアだけを表示する
#
# 【期待する出力】
#   合計: 705
#   平均: 70.5
#   80点以上: [82, 91, 88, 95]
#
# 【Pythonのリストメモ】
#   scores = [1, 2, 3]     ← PHPの配列と同じ感覚
#   len(scores)            ← 要素数を取得
#   sum(scores)            ← 合計を取得（Pythonの組み込み関数）
#   for s in scores:       ← リストをループ
#
# 【ポイント】
#   80点以上のスコアを集めるには、
#   空のリストを用意して条件に合うものを追加していく方法があります。
#   リストに要素を追加するには .append() を使います。
#
# ============================================

scores = [45, 82, 91, 60, 73, 55, 88, 76, 40, 95]

# 1. 合計を表示
# sam ==0
# for s in scores:
#     sam = s +sam 

# print(sam)
print(sum(scores))

# 2. 平均を表示
print(sum(scores)/len(scores))

# 3. 80点以上のスコアだけ表示
max =[]
for s in scores:
    if 80<= s:
        max.append(s)


print(max)
